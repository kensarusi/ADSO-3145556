package Shopping.BackendCart.dto;

/**
 * Person DTO - Data Transfer Object for Person
 * Contains only the necessary fields for business logic (no IDs for creation)
 */
public class PersonDTO {

    private String firstName;
    private String lastName;
    private String email;
    private String phone;
    private String address;

    // Constructors
    public PersonDTO() {}

    public PersonDTO(String firstName, String lastName, String email, String phone, String address) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.phone = phone;
        this.address = address;
    }

    // Getters and setters
    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
}
